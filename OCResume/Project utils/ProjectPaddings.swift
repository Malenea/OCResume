import UIKit

// MARK: Project paddings
struct PPaddings {

    // The basic padding should be of minimum 8.0 for anything
    public static let basicPadding: CGFloat = 8.0

    // Normal in between views and components padding
    public static let interPadding: CGFloat = 16.0

    // The minimum recommended padding for components
    public static let componentPadding: CGFloat = 48.0
    public static let fullScreenComponentPadding: CGFloat = 200.0
    public static let componentCornerRadiusPadding: CGFloat = 24.0

}
